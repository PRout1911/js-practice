//while
let i = 0
while (i <= 15) {
    console.log(`Value of i: ${i}`);
    i+=2
}

let myArr = ['Maths', 'Science', 'Hindi', 'English']
let arr = 0
while (arr <= myArr.length) {
    console.log(`Value if ${arr} in array is: ${myArr[arr]}`);
    arr++
}

//do - while

let index = 1
do {
    console.log(`Value is: ${index}`);
    index++
} while (index <= 10);